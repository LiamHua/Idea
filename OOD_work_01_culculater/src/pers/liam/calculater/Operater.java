package pers.liam.calculater;

public class Operater {
    public static double add(double k,double i){
        return k+i;
    }

    public static double sub(double k,double i){
        return  k-i;
    }

    public static double mul(double k,double i){
        return  k*i;
    }

    public static double div(double k,double i){
        return  k/i;
    }
}
